export function findAudatexPartByCode(code: string): AudatexPartCode | undefined {
  const normalized = code.trim().toUpperCase();
  return AUDATEX_PART_CODES.find(p => p.code === normalized || p.code.padStart(4, '0') === normalized);
}

export function findAudatexPartByDescription(desc: string): AudatexPartCode | undefined {
  const normalized = desc.trim().toLowerCase();
  if (normalized.length < 2) return undefined;
  
  // Exact match first
  let found = AUDATEX_PART_CODES.find(p => p.description.toLowerCase() === normalized);
  if (found) return found;

  // Contains match
  return AUDATEX_PART_CODES.find(p => p.description.toLowerCase().includes(normalized));
}

export function searchAudatexParts(query: string): AudatexPartCode[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return [];

  return AUDATEX_PART_CODES.filter(p => 
    p.code.toLowerCase().includes(normalized) || 
    p.description.toLowerCase().includes(normalized)
  ).slice(0, 8); // return top 8 matches for UX usability
}
